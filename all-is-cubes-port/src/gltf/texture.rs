/// Coordinate system marker type for positions in the texture atlas generated by
/// [`GltfTextureAllocator`].
pub(crate) struct AtlasUv;

/// The mapping from [`GltfTextureAllocator`] allocations to texture atlas UV coordinates.
pub(crate) struct UvMap {
    /// Maps each [`PlaneId`] to its origin (upper-left corner as viewed in an image viewer)
    /// in the atlas, in texels.
    positioning: HashMap<PlaneId, Point2D<u32, texture::TexelUnit>>,

    /// Scale factor from texels ([`texture::TexelUnit`]) to 0-1 UV coordinates as used by glTF.
    /// Stored as f64 to maximize precision up to the final result (TODO: is this necessary?)
    scale: Scale<f64, texture::TexelUnit, AtlasUv>,

    /// Texture coordinates of the center of a texel that is always opaque white.
    ///
    /// This special texel is used to allow mixing vertex colors with textures in the same mesh,
    /// by making one white while the other is meaningful.
    white: [Lef32; 2],
}

impl UvMap {
    /// Vertex tests need a `UvMap` to check the math with,
    /// but `UvMap`'s details otherwise belong to the texture allocator and atlas builder,
    /// so this function is for that special case only.
    #[cfg(test)]
    pub(in crate::gltf) fn new_for_testing(
        plane_id: PlaneId,
        offset: Point2D<u32, texture::TexelUnit>,
        white: Point2D<f32, AtlasUv>,
    ) -> Self {
        Self {
            positioning: HashMap::from([(plane_id, offset)]),
            // normally this would be less than 1 to create 0-1 coordinates,
            // but doing this instead keeps the test simple.
            scale: Scale::new(1.0),
            white: white.to_array().map(Lef32::new),
        }
    }

    /// Convert a point in a plane allocation to a UV point in the atlas texture.
    ///
    /// Panics if `plane_id` is not in the map.
    pub fn plane_to_atlas(
        &self,
        plane_id: PlaneId,
        texel_in_plane: Point2D<f32, texture::TexelUnit>, // TODO: more specific unit
    ) -> Point2D<f32, AtlasUv> {
        let origin_of_plane_in_atlas = self.positioning[&plane_id];
        self.scale
            .transform_point(
                origin_of_plane_in_atlas.map(f64::from) + texel_in_plane.map(f64::from).to_vector(),
            )
            .cast::<f32>()
    }

    /// Returns the center of a texel that is opaque white.
    pub fn white(&self) -> [Lef32; 2] {
        self.white
    }
}

